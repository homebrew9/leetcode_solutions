func minimumOperations(nums []int) int {
    N := len(nums)
    res := 0
    i, j := 0, N - 1
    for i < j {
        if nums[i] == nums[j] {
            i++
            j--
        } else if nums[i] > nums[j] {
            for j > i && nums[j] < nums[i] {
                j--
                nums[j] += nums[j+1]
                res++
            }
        } else if nums[j] > nums[i] {
            for i < j && nums[i] < nums[j] {
                i++
                nums[i] += nums[i-1]
                res++
            }
        }
    }
    return res
}

