from typing import List

class Solution:
    def minBitwiseArray(self, nums: List[int]) -> List[int]:
        def solve(n):
            # If bin(n) = '10111', then traverse from right to left and find the first 1
            # that has a 0 to its left. Change that 1 to 0, retaining the other bits as is.
            # If bin(n) = '11111', then the answer is '1111'
            if n % 2 == 0:
                return -1
            s = bin(n)[2:]
            i = s.rfind('0')
            arr = list(s)
            arr[i+1] = '0'
            return int(''.join(arr), 2)
        res = list()
        for num in nums:
            val = solve(num)
            res.append(val)
        return res

