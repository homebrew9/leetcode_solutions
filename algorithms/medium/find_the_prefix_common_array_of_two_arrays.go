func findThePrefixCommonArray(A []int, B []int) []int {
    hshA, hshB := make(map[int]int), make(map[int]int)
    N := len(A)
    res := []int{}
    for i := 0; i < N; i++ {
        hshA[A[i]]++
        hshB[B[i]]++
        cnt := 0
        for k, _ := range hshA {
            _, ok := hshB[k]
            if ok {
                cnt++
            }
        }
        res = append(res, cnt)
    }
    return res
}

