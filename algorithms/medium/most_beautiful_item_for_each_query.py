from typing import List

class Solution:
    def maximumBeauty(self, items: List[List[int]], queries: List[int]) -> List[int]:
        N = len(items)
        items.sort()
        for i in range(1, N):
            items[i][1] = max(items[i-1][1], items[i][1])
        #print(items)
        res = list()
        for q in queries:
            left, right = 0, N - 1
            while left <= right:
                mid = (left + right) // 2
                val = items[mid][0]
                if val <= q:
                    left = mid + 1
                else:
                    right = mid - 1
            res += [0 if right == -1 else items[right][1]]
        return res

