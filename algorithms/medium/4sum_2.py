from typing import List

class Solution:
    def fourSumCount(self, nums1: List[int], nums2: List[int], nums3: List[int], nums4: List[int]) -> int:
        hsh = dict()
        N = len(nums1)
        for i in range(N):
            for j in range(N):
                total = nums1[i] + nums2[j]
                if total in hsh:
                    hsh[total] += 1
                else:
                    hsh[total] = 1
        res = 0
        for i in range(N):
            for j in range(N):
                total = nums3[i] + nums4[j]
                if -total in hsh:
                    res += hsh[-total]
        return res

