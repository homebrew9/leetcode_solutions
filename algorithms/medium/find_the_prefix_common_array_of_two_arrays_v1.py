#
# No need for Brute Force or set-based comparisons. Since the constraints are small, we
# can use a frequency array to assign frequency of each element and increment the common
# counter if frequency equals 2. Note that len(A) = len(B) <= 50. So our frequency array
# will have 51 elements at the most. TC = O(N), SC = O(N).
#
from typing import List

class Solution:
    def findThePrefixCommonArray(self, A: List[int], B: List[int]) -> List[int]:
        N = len(A)
        arr = [0 for _ in range(N + 1)]
        res = list()
        common = 0
        for a, b in zip(A, B):
            arr[a] += 1
            if arr[a] == 2:
                common += 1
            arr[b] += 1
            if arr[b] == 2:
                common += 1
            res.append(common)
        return res

